def title_from_identifier(identifier):
    return FIELD_EN.get(identifier, identifier.replace("_", " ").title())


def zh_from_identifier(identifier):
    if identifier in FIELD_ZH:
        return FIELD_ZH[identifier]
    return " ".join(TOKEN_ZH.get(part, part.upper() if len(part) == 1 else part) for part in identifier.split("_"))


def hierarchy_label(path):
    parts = path.split(".")
    labels = [ROOT_EN.get(parts[0], title_from_identifier(parts[0]))]
    for part in parts[1:]:
        if part.startswith("wcol_"):
            widget = title_from_identifier(part[5:])
            if widget != "State":
                widget = f"{widget} Widget"
            labels.append(widget)
        else:
            labels.append(title_from_identifier(part))
    return "> ".join(labels)


def hierarchy_label_zh(path):
    parts = path.split(".")
    labels = [ROOT_ZH.get(parts[0], zh_from_identifier(parts[0]))]
    for part in parts[1:]:
        if part.startswith("wcol_"):
            labels.append(WIDGET_ZH.get(part[5:], zh_from_identifier(part[5:])))
        else:
            labels.append(zh_from_identifier(part))
    return "> ".join(labels)


def load_theme_paths():
    source = Path(__file__).with_name("__init__.py").read_text(encoding="utf-8")
    match = re.search(r"THEME_PATH_LABELS_EN = (\{.*?\n\})", source, re.S)
    if not match:
        return []
    return sorted(literal_eval(match.group(1)).keys())


def build_zh_cn():
    entries = dict(STATIC_ZH)
    entries.update(AREA_ZH)
    entries.update(REGION_ZH)
    entries.update(MODE_ZH)

    for area_en, area_zh in AREA_ZH.items():
        entries[f"{area_en} Boundary"] = f"{area_zh}边界"
        for region_en, region_zh in REGION_ZH.items():
            entries[f"{area_en} - {region_en}"] = f"{area_zh} - {region_zh}"

    entries.update({
        "Top UI Area": "顶部界面区域",
        "Bottom Status/Screen Edge": "底部状态/屏幕边缘",
        "Area Separator / Screen Gap": "区域分隔/屏幕间隙",
        "Unknown Screen Position": "未知屏幕位置",
        "Screenshot Lookup Area": "截图查找区域",
    })

    for path in load_theme_paths():
        entries[hierarchy_label(path)] = hierarchy_label_zh(path)

    translated = {}
    for msgid, msgstr in entries.items():
        translated[(DEFAULT_CONTEXT, msgid)] = msgstr
        translated[(OPERATOR_CONTEXT, msgid)] = msgstr
    return translated


